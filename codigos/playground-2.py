valorDeA = 3
valorDeB = 5

# a menor que b
print(valorDeA < valorDeB)

#a maior que b
print(valorDeA > valorDeB)

#a menor igual a b
print(valorDeA <= valorDeB)

#a maior igual a b
print(valorDeA >= valorDeB)

#a igual de b
print(valorDeA == valorDeB)

#a diferente de b)
print(valorDeA != valorDeB)

# a identico a b / para objetos
print(valorDeA is valorDeB)

# a nao identico a b / para objetos
print(valorDeA is not valorDeB)
